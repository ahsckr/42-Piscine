int	ft_is_prime(int nb)
{
	int	i;
	int	a;

	a = 1;
	i = 2;
	if (nb == 2 || nb == 3)
	{
		return (1);
	}
	while (i <= nb / 2)
	{
		if ((nb % i == 0))
		{
			return (0);
		}
		i++;
	}
	if (2 > nb)
	{
		a = 0;
	}
	return (a);
}
